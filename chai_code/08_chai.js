//Dates
let date1 = new Date()
console.log(date1) //current date and time

console.log(date1.toString()) //full date string
console.log(date1.toDateString()) //date string
console.log(date1.toISOString()) //ISO date string
console.log(date1.toLocaleDateString()) //locale date string
console.log(date1.toLocaleTimeString()) //locale time string

console.log(typeof date1) //object